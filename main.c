uint16_t number[12][128] = {0};

void init_number(void);
void print_number(void);


int main(int argc, char** argv)
{
    init_number();
    print_number();
    return 0;
}

void init_number(void)
{
    int i;      // 每一个字符
    int j;      // 字符的每一个  byte
    int k;      // byte 的每一个 bit

    for(i = 0; i < 12; i++)
    {
        for(j = 0; j < 16; j++)
        {
            for(k = 0; k < 8; k++)
            {
                if(code[i][j] & (0x1 << k))
                {
                    number[i][8*k+j/2+(j%2)*64] = 'X';
                }
                else
                {
                    number[i][8*k+j/2+(j%2)*64] = ' ';
                }
            }
        }
    }

}

void print_number(void)
{
    int i;
    int j;

    for(i = 0; i < 12; i++)
    {
        for(j = 0; j < 128; j++)
        {
            printf("%c", number[i][j]);
            if(j % 8 == 7)
            {
                printf("\n");
            }
        }
    }
}
